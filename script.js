var currentLang = localStorage.getItem('pre_lang') || 'sq';

function t(key) {
  return lang[currentLang] && lang[currentLang][key] !== undefined ? lang[currentLang][key] : (lang['sq'][key] || key);
}

function badgeLabel(badge) {
  if (badge === 'badge-red') return t('badge.sale');
  if (badge === 'badge-blue') return t('badge.rent');
  if (badge === 'badge-gold') return t('badge.exclusive');
  return '';
}

function applyTranslation() {
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var key = el.getAttribute('data-i18n');
    var val = t(key);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.setAttribute('placeholder', val);
    } else if (el.tagName === 'META') {
      el.setAttribute('content', val);
    } else if (el.tagName === 'OPTION') {
      el.textContent = val;
    } else {
      el.innerHTML = val;
    }
  });
  document.querySelectorAll('[data-i18n-alt]').forEach(function(el) {
    el.setAttribute('alt', t(el.getAttribute('data-i18n-alt')));
  });
  document.querySelectorAll('[data-i18n-title]').forEach(function(el) {
    el.setAttribute('title', t(el.getAttribute('data-i18n-title')));
  });
}

function setLang(l) {
  currentLang = l;
  localStorage.setItem('pre_lang', l);
  document.querySelectorAll('.langs a').forEach(function(a) { a.classList.remove('active'); });
  var el = document.querySelector('.langs a[data-lang="' + l + '"]');
  if (el) el.classList.add('active');
  document.documentElement.lang = l;
  applyTranslation();
  
  updateFavUI();
  if (typeof applyFilters === 'function') applyFilters();
}


function getFavorites() {
  var stored = localStorage.getItem('pre_favorites');
  return stored ? JSON.parse(stored) : [];
}

function saveFavorites(ids) {
  localStorage.setItem('pre_favorites', JSON.stringify(ids));
}

function toggleFav(id) {
  var favs = getFavorites();
  var idx = favs.indexOf(id);
  if (idx > -1) { favs.splice(idx, 1); } else { favs.push(id); }
  saveFavorites(favs);
  updateFavUI();
  return favs.indexOf(id) > -1;
}

function isFav(id) {
  return getFavorites().indexOf(id) > -1;
}

function updateFavUI() {
  document.querySelectorAll('.heart').forEach(function(el) {
    var id = el.getAttribute('data-id');
    if (isFav(id)) {
      el.innerHTML = '&#9829;';
      el.classList.add('fav-active');
    } else {
      el.innerHTML = '&#9825;';
      el.classList.remove('fav-active');
    }
  });
  
  if (document.getElementById('favGrid')) {
    renderFavorites();
  }
}


function matchRange(val, range) {
  if (range === 'all') return true;
  var p = range.split('-');
  return val >= +p[0] && val <= +p[1];
}

function applyFilters() {
  var searchInput = document.querySelector('.search-bar input');
  if (!searchInput) return;
  var search = searchInput.value.toLowerCase();
  var type = 'all', cat = 'all';
  var typeSel = document.querySelector('.filter-type');
  var catSel = document.querySelector('.filter-cat');
  if (typeSel) type = typeSel.value;
  if (catSel) cat = catSel.value;
  var sels = document.querySelectorAll('.filter-group select:not(.filter-type):not(.filter-cat)');
  if (sels.length < 3) return;
  var priceRange = sels[0].value, areaRange = sels[1].value, loc = sels[2].value;

  var cards = document.querySelectorAll('.card');
  var visible = 0;

  cards.forEach(function(c) {
    var title = c.querySelector('h3') ? c.querySelector('h3').textContent.toLowerCase() : '';
    var locText = c.querySelector('.loc') ? c.querySelector('.loc').textContent.toLowerCase() : '';
    var priceText = c.querySelector('.price') ? c.querySelector('.price').textContent.toLowerCase() : '';
    var match = (!search || title.indexOf(search) !== -1 || locText.indexOf(search) !== -1 || priceText.indexOf(search) !== -1) &&
                (type === 'all' || c.getAttribute('data-type') === type) &&
                (cat === 'all' || c.getAttribute('data-category') === cat) &&
                matchRange(+c.getAttribute('data-price'), priceRange) &&
                matchRange(+c.getAttribute('data-area'), areaRange) &&
                (loc === 'all' || c.getAttribute('data-location') === loc);
    c.style.display = match ? '' : 'none';
    if (match) visible++;
  });

  var countEl = document.querySelector('.props-header span');
  if (countEl) countEl.innerHTML = visible + ' <span data-i18n="props.count">' + t('props.count') + '</span>';
  var nr = document.getElementById('noResults');
  if (nr) nr.style.display = visible ? 'none' : '';
}

function resetFilters() {
  var searchInput = document.querySelector('.search-bar input');
  if (searchInput) searchInput.value = '';
  document.querySelectorAll('.filter-group select').forEach(function(s) { s.selectedIndex = 0; });
  if (typeof applyFilters === 'function') applyFilters();
}


